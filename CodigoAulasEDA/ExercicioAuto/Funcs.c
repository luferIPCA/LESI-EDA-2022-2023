#include <stdio.h>
#include <stdbool.h>

/**
 * @brief Verifica se determinado valor é impar.
 * 
 * @param x
 * @return 
 * @author lufer
 *
 */
bool IsOdd(int x) {
	return (x % 2 != 0);
}


